var $ = require('jquery')
if (typeof window !== 'undefined') {
    window.addEventListener('DOMContentLoaded', event => {
        $(document).ready(function () {
            $('#sidebarCollapse').on('click', function () {
                $('#sidebar').toggleClass('active');
                $(this).toggleClass('active');
            });
        });
    });
}
